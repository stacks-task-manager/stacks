/**
 * Migration Compilation Script
 *
 * This script compiles the TypeScript init_schema.ts file into a CommonJS migration
 * file that can be used with Sequelize CLI.
 *
 * Usage:
 *   yarn compile:migration
 *   or
 *   node compile-migration.js
 *
 * Input:  src/init_schema.ts
 * Output: migrations/000_init_schemas.js
 */

import { build } from "esbuild";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { existsSync, mkdirSync } from "fs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const srcFile = join(__dirname, "src", "init_schema.ts");
const outDir = join(__dirname, "migrations");
const outFile = join(outDir, "000_init_schemas.cjs");

// Ensure migrations directory exists
if (!existsSync(outDir)) {
    mkdirSync(outDir, { recursive: true });
}

async function compileMigration() {
    try {
        console.log("🔨 Compiling migration file...");
        console.log(`📁 Source: ${srcFile}`);
        console.log(`📁 Output: ${outFile}`);

        await build({
            entryPoints: [srcFile],
            bundle: true,
            platform: "node",
            target: "node18",
            format: "cjs", // CommonJS format for Sequelize migrations
            outfile: outFile,
            external: ["sequelize", "sequelize-cli", "pg", "pg-hstore"],
            banner: {
                js: "// Auto-generated migration file from init_schema.ts\n// Do not edit this file directly\n",
            },

            resolveExtensions: [".ts", ".js"],
            loader: {
                ".ts": "ts",
            },
            tsconfig: join(__dirname, "tsconfig.json"),
            minify: false,
            sourcemap: false,
            metafile: false,
        });

        console.log("✅ Migration compiled successfully!");
        console.log(`📄 Output file: ${outFile}`);
    } catch (error) {
        console.error("❌ Compilation failed:", error);
        process.exit(1);
    }
}

compileMigration();
